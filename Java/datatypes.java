public class Datatypes{
	public static void main(String[] args){
		byte a = 1;		//byte size - 1 byte
		short b = 5;		//short size - 2 byte
		int c = 10;		//int size - 4 byte
		double d = 10.5;	//double size - 8 byte
		long e = 123456;	//long size - 8 byte
		boolean f = true;	//boolean size - 1 byte
		char g = 'g';		//char size - 2 byte
		
		String str = "Hari";	//String non Primitive datatype, size - 32 byte.

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);

		System.out.println(str);
	}
}
