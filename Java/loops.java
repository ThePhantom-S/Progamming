public class loops {
    public static void main(String[] args) {
        //for loop
        System.out.println("For loop:");
        for(int i=0; i<5; i++){
            System.out.println(i);
        }

        //while loop
        System.out.println("While loop:");
        int j = 0;
        while(j<5){
            System.out.println(j);
            j++;
        }

        //do-while loop
        System.out.println("Do-While loop:");
        int k = 0;
        do{
            System.out.println(k);
            k++;
        }while(k<5);

    }
}
