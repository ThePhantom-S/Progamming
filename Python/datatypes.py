#all the datatypes in python are as follows:
#1. int
#2. float
#3. complex
#4. bool
#5. str
#6. list
#7. tuple
#8. set
#9. dict
#1. int: it is used to store whole numbers
a = 10
print(type(a))
#2. float: it is used to store decimal numbers
b = 3.14
print(type(b))
#3. complex: it is used to store complex numbers
c = 2 + 3j
print(type(c))
#4. bool: it is used to store boolean values
d = True
print(type(d))
#5. str: it is used to store string values
e = "Hello, World!"
print(type(e))
#6. list: it is used to store a collection of items
f = [1, 2, 3, 4, 5]
print(type(f))
#7. tuple: it is used to store a collection of items, but it is immutable
g = (1, 2, 3, 4, 5)
print(type(g))
#8. set: it is used to store a collection of unique items
h = {1, 2, 3, 4, 5}
print(type(h))
#9. dict: it is used to store a collection of key-value pairs
i = {"name": "Alice", "age": 30, "city": "New York"}
print(type(i))


